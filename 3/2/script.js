function humanYearsToDogYears(){
    let humanYears = prompt("Concert human age to dog age!");
    let humanTearsConverted = parseInt (humanYears);
    let dogAge = humanTearsConverted * 7;
    let result = `Your dog age is - ${dogAge}`;
    switch (dogAge) {
        case dogAge:
            dogAge === Number;
            console.log(result);
            break;
    
        default:
            console.log("Enter a numer!");
            break;
    }
    
}

humanYearsToDogYears();

function dogYearsToHumanYears(){
    let dogYears = prompt("Concert dog age to human age!");
    let dogTearsConverted = parseInt (dogYears);
    let humanAge = dogTearsConverted / 7;
    let result = `Your human age is - ${humanAge}`;
    switch (humanAge) {
        case humanAge:
            humanAge === Number;
            console.log(result);
            break;
    
        default:
            console.log("Enter a numer!");
            break;
    }
    
}

dogYearsToHumanYears();
