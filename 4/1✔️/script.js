function tellStory(name, mood, activity) {
    return `This is ${name}. ${name} is a nice person. Today they are ${mood}. They are ${activity} all day. The end.`;
}

console.log(tellStory("Nikola", "happy", "playing"));
console.log(tellStory("Lio", "sad", "working"));
console.log(tellStory("Slav", "bad", "working"));